const createProjectScreenshots = (folder, title, scenes = []) => {
    if (!folder) {
        return [];
    }

    const fallbackScenes = [
        { title: 'Overview', description: `Main interface preview for ${title}.` },
        { title: 'Workflow', description: `Core product workflow inside ${title}.` },
        { title: 'Detail', description: `Supporting screen or internal view from ${title}.` }
    ];

    const activeScenes = scenes.length > 0 ? scenes : fallbackScenes;

    return activeScenes.map((scene, index) => ({
        image: `img/${folder}/${index + 1}.png`,
        title: `${title} - ${scene.title}`,
        description: scene.description
    }));
};

window.portfolioProjects = window.portfolioProjects.map((project) => {
    const detail = projectDetailMap[project.slug] || {};
    const folder = detail.mediaFolder || '';
    const tags = Array.isArray(project.tags) ? project.tags : [];

    return {
        ...project,
        ...detail,
        categories: detail.categories || project.categories || [],
        liveUrl: detail.liveUrl || project.liveUrl,
        domain: detail.domain || project.domain || project.category,
        coverImage: folder ? `img/${folder}/1.png` : (detail.coverImage || project.coverImage || ''),
        screenshots: folder ? createProjectScreenshots(folder, project.title, detail.scenes || []) : (detail.screenshots || project.screenshots || []),
        overview: detail.overview || project.overview || [
            project.summary,
            `Built as a ${project.category.toLowerCase()} project using ${tags.join(', ')}.`
        ],
        highlights: detail.highlights || project.highlights || [
            `Delivered as a ${project.status.toLowerCase()} project in the ${project.category.toLowerCase()} category.`,
            `Main stack represented by ${tags.slice(0, 4).join(', ')}.`,
            `Repository visibility: ${project.repoVisibility}.`
        ],
        stack: detail.stack || project.stack || {
            'Primary Stack': tags.join(', ') || 'Not specified',
            'Repository Type': project.repoVisibility,
            'Delivery Status': project.status,
            Domain: detail.domain || project.category
        }
    };
});
